public class PartA {
    public static void main(String[] args) {
        double radius = 2;
        double area = Math.PI * radius * radius;
        System.out.println("The area of the circle with a radius of 2 is: " + area); 
        
        double randomVal;
        randomVal = Math.random();
        System.out.println("A random value between 0 and 1 is: " + randomVal);
    }
}